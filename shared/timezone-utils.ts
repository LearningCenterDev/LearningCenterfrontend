export function getTimezoneFromLocation(state: string | null | undefined, country: string | null | undefined): string {
  const normalizedCountry = country?.trim().toUpperCase() || '';
  const normalizedState = state?.trim().toUpperCase() || '';
  
  if (normalizedCountry === 'US' || normalizedCountry === 'USA' || normalizedCountry === 'UNITED STATES') {
    if (normalizedState && US_STATE_TIMEZONES[normalizedState]) {
      return US_STATE_TIMEZONES[normalizedState];
    }
    return 'America/New_York';
  }
  
  if (normalizedCountry === 'CA' || normalizedCountry === 'CANADA') {
    if (normalizedState && CANADIAN_PROVINCE_TIMEZONES[normalizedState]) {
      return CANADIAN_PROVINCE_TIMEZONES[normalizedState];
    }
    return 'America/Toronto';
  }
  
  for (const [key, tz] of Object.entries(COUNTRY_TIMEZONES)) {
    if (key.toUpperCase() === normalizedCountry) {
      return tz;
    }
  }
  
  return 'UTC';
}

export function formatTimezoneDisplay(timezone: string): string {
  try {
    const now = new Date();
    const formatter = new Intl.DateTimeFormat('en-US', {
      timeZone: timezone,
      timeZoneName: 'short'
    });
    const parts = formatter.formatToParts(now);
    const tzPart = parts.find(p => p.type === 'timeZoneName');
    const abbr = tzPart?.value || '';
    
    const offset = getTimezoneOffset(timezone);
    const displayName = timezone.replace(/_/g, ' ').split('/').pop() || timezone;
    
    return `${displayName} (${abbr}, UTC${offset})`;
  } catch {
    return timezone;
  }
}

export function getTimezoneOffset(timezone: string): string {
  try {
    const now = new Date();
    const utcDate = new Date(now.toLocaleString('en-US', { timeZone: 'UTC' }));
    const tzDate = new Date(now.toLocaleString('en-US', { timeZone: timezone }));
    const diffMinutes = (tzDate.getTime() - utcDate.getTime()) / (1000 * 60);
    const hours = Math.floor(Math.abs(diffMinutes) / 60);
    const minutes = Math.abs(diffMinutes) % 60;
    const sign = diffMinutes >= 0 ? '+' : '-';
    return `${sign}${hours.toString().padStart(2, '0')}:${minutes.toString().padStart(2, '0')}`;
  } catch {
    return '+00:00';
  }
}
